/**
 * extract-college-colors.mjs
 *
 * Scans /public for college logo files (*-logo.{png,jpg,svg}),
 * extracts the dominant vibrant color from each, and writes
 * lib/collegeColors.generated.ts for use in collegeAtlasConfigs.ts.
 *
 * Run automatically via predev / prebuild npm hooks.
 */

import sharp from "sharp";
import { readdir, writeFile } from "fs/promises";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const PUBLIC_DIR = join(__dirname, "../public");
const OUT_FILE = join(__dirname, "../lib/collegeColors.generated.ts");
const DEFAULT_COLOR = "#1e3a5f";

// ── Color utilities ─────────────────────────────────────────────────────────

function rgbToHsl(r, g, b) {
  r /= 255; g /= 255; b /= 255;
  const max = Math.max(r, g, b), min = Math.min(r, g, b);
  const l = (max + min) / 2;
  if (max === min) return [0, 0, l];
  const d = max - min;
  const s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
  let h = 0;
  if (max === r) h = ((g - b) / d + (g < b ? 6 : 0)) / 6;
  else if (max === g) h = ((b - r) / d + 2) / 6;
  else h = ((r - g) / d + 4) / 6;
  return [h * 360, s, l];
}

function toHex(r, g, b) {
  return "#" + [r, g, b].map(v => Math.round(v).toString(16).padStart(2, "0")).join("");
}

/**
 * Given raw RGBA pixel buffer, return the dominant vibrant hex color,
 * or null if the image appears monochromatic (white, black, or grey).
 */
function extractDominantColor(buffer) {
  const pixels = [];

  for (let i = 0; i < buffer.length; i += 4) {
    const r = buffer[i], g = buffer[i + 1], b = buffer[i + 2], a = buffer[i + 3];

    // Skip transparent pixels
    if (a < 128) continue;

    const [, s, l] = rgbToHsl(r, g, b);

    // Skip near-white, near-black, and near-grey
    if (l > 0.88) continue;   // near-white
    if (l < 0.08) continue;   // near-black
    if (s < 0.15) continue;   // near-grey / desaturated

    pixels.push({ r, g, b, s, l });
  }

  if (pixels.length === 0) return null;

  // Bucket by hue in 15-degree increments, weighted by saturation
  const buckets = {};
  for (const p of pixels) {
    const [h] = rgbToHsl(p.r, p.g, p.b);
    const bucket = Math.floor(h / 15) * 15;
    if (!buckets[bucket]) buckets[bucket] = { count: 0, r: 0, g: 0, b: 0 };
    buckets[bucket].count++;
    buckets[bucket].r += p.r;
    buckets[bucket].g += p.g;
    buckets[bucket].b += p.b;
  }

  // Find most populated bucket
  const best = Object.values(buckets).sort((a, b) => b.count - a.count)[0];
  const avgR = best.r / best.count;
  const avgG = best.g / best.count;
  const avgB = best.b / best.count;

  return toHex(avgR, avgG, avgB);
}

// ── Main ─────────────────────────────────────────────────────────────────────

async function processLogo(filePath) {
  try {
    const { data, info } = await sharp(filePath, { density: 150 })
      .resize(80, 80, { fit: "inside", withoutEnlargement: false })
      .ensureAlpha()
      .raw()
      .toBuffer({ resolveWithObject: true });

    return extractDominantColor(data);
  } catch {
    return null;
  }
}

async function main() {
  const files = await readdir(PUBLIC_DIR);

  // Non-college assets to skip
  const EXCLUDE = new Set(["kallipolis"]);

  // Only process stacked logos (not -long variants) to get the primary mark
  const logoFiles = files.filter(f => {
    if (!/^[a-z0-9-]+-logo\.(png|jpg|jpeg|svg|gif)$/i.test(f)) return false;
    if (f.includes("-logo-long") || f.includes("-logo-2")) return false;
    const id = f.match(/^(.+)-logo\./)?.[1];
    return id && !EXCLUDE.has(id);
  });

  const results = {};

  for (const file of logoFiles) {
    const match = file.match(/^(.+)-logo\./);
    if (!match) continue;
    const collegeId = match[1];
    const filePath = join(PUBLIC_DIR, file);
    const color = await processLogo(filePath);
    results[collegeId] = color ?? DEFAULT_COLOR;
    console.log(`  ${collegeId}: ${results[collegeId]}${color ? "" : " (fallback)"}`);
  }

  const entries = Object.entries(results)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([id, hex]) => `  "${id}": "${hex}",`)
    .join("\n");

  const output = `// AUTO-GENERATED by scripts/extract-college-colors.mjs — do not edit manually
// Re-generated automatically on \`npm run dev\` and \`npm run build\`.

export const COLLEGE_COLORS: Record<string, string> = {
${entries}
};
`;

  await writeFile(OUT_FILE, output, "utf8");
  console.log(`\nWrote ${Object.keys(results).length} college colors → lib/collegeColors.generated.ts`);
}

main().catch(err => { console.error(err); process.exit(1); });
